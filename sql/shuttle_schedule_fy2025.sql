-- 愛知工業大学 シャトルバス運行日程（令和7年度：2025年4月～2026年3月）
-- 出典: 令和7年3月3日発表の運行日程表
-- このテーブルは日付ごとのダイヤ種別（A/B/C）を管理します

USE ait_transport;

-- 運行日程テーブルの作成
CREATE TABLE IF NOT EXISTS shuttle_schedule (
    id INT PRIMARY KEY AUTO_INCREMENT,
    operation_date DATE UNIQUE NOT NULL COMMENT '運行日',
    dia_type ENUM('A', 'B', 'C', 'holiday') NOT NULL COMMENT 'ダイヤ種別: A=授業期間平日, B=土曜日, C=学校休業期間, holiday=運行なし',
    fiscal_year INT NOT NULL COMMENT '会計年度（2025=令和7年）',
    is_operational BOOLEAN DEFAULT TRUE COMMENT '運行フラグ',
    remarks VARCHAR(255) COMMENT '備考',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_date (operation_date),
    INDEX idx_dia_type (dia_type),
    INDEX idx_fiscal_year (fiscal_year)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 既存データをクリア（更新時用）
TRUNCATE TABLE shuttle_schedule;

